# Dar la suma, el producto y el promedio de tres números.
float1 = float(input("Ingrese el primer número: "))
float2 = float(input("Ingrese el segundo número: "))
float3 = float(input("Ingrese el tercer número: "))
suma = float1 + float2 + float3
producto = float1 * float2 * float3
promedio = suma / 3
print("La suma es:", suma)
print("El producto es:", producto)
print("El promedio es:", promedio)

